DB_PATH = os.path.join(os.path.dirname(__file__), "idx_stocks.db")


def get_db_connection():
    conn = sqlite3.connect(DB_PATH)
    conn.execute("""
        CREATE TABLE IF NOT EXISTS stock_data (
            ticker TEXT,
            date TEXT,
            open REAL,
            high REAL,
            low REAL,
            close REAL,
            volume INTEGER,
            PRIMARY KEY (ticker, date)
        )
    """)
    conn.commit()
    return conn


def fetch_ticker(ticker: str, period: str = "1y", interval: str = "1d") -> pd.DataFrame:
    """Fetch OHLCV data for a single IDX ticker."""
    try:
        t = yf.Ticker(ticker)
        df = t.history(period=period, interval=interval)
        if df.empty:
            print(f"[WARNING] No data for {ticker}")
            return pd.DataFrame()
        df.index = pd.to_datetime(df.index).tz_localize(None)
        df = df[["Open", "High", "Low", "Close", "Volume"]].copy()
        df.columns = ["open", "high", "low", "close", "volume"]
        df["ticker"] = ticker
        return df
    except Exception as e:
        print(f"[ERROR] Failed to fetch {ticker}: {e}")
        return pd.DataFrame()


def fetch_and_store(tickers: list = None, period: str = "1y"):
    """Fetch multiple tickers and store in SQLite."""
    if tickers is None:
        tickers = DEFAULT_WATCHLIST

    conn = get_db_connection()
    results = {}

    for ticker in tickers:
        print(f"Fetching {ticker}...")
        df = fetch_ticker(ticker, period=period)
        if df.empty:
            continue

        df_store = df.reset_index()
        df_store["date"] = df_store["Date"].astype(str)
        df_store = df_store[["ticker", "date", "open", "high", "low", "close", "volume"]]
        df_store.to_sql("stock_data", conn, if_exists="replace" if ticker == tickers[0] else "append",
                        index=False, method="replace")

        results[ticker] = df
        print(f"  ✓ {ticker}: {len(df)} rows")

    conn.commit()
    conn.close()
    print(f"\n✅ Done! Fetched {len(results)} tickers.")
    return results


def load_from_db(ticker: str) -> pd.DataFrame:
    """Load stored data from SQLite."""
    conn = get_db_connection()
    df = pd.read_sql(
        "SELECT * FROM stock_data WHERE ticker = ? ORDER BY date",
        conn, params=(ticker,)
    )
    conn.close()
    if df.empty:
        return df
    df["date"] = pd.to_datetime(df["date"])
    df.set_index("date", inplace=True)
    return df


def get_stock_info(ticker: str) -> dict:
    """Get company info for a ticker."""
    try:
        t = yf.Ticker(ticker)
        info = t.info
        return {
            "name": info.get("longName", ticker),
            "sector": info.get("sector", "N/A"),
            "industry": info.get("industry", "N/A"),
            "market_cap": info.get("marketCap", 0),
            "pe_ratio": info.get("trailingPE", None),
            "52w_high": info.get("fiftyTwoWeekHigh", None),
            "52w_low": info.get("fiftyTwoWeekLow", None),
        }
    except:
        return {"name": ticker}


